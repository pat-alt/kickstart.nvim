#' Sum Two Numbers
#'
#' @param a A numeric value.
#' @param b A numeric value.
#' @return The sum of `a` and `b`.
#' @examples
#' mysum(2, 3)
mysum <- function(a, b) {
    return(sum(a, b))
}

#' Multiply Two Numbers
#'
#' @param a A numeric value.
#' @param b A numeric value.
#' @return The product of `a` and `b`.
#' @examples
#' myprod(2, 3)
myprod <- function(a, b) {
    return(a * b)
}
